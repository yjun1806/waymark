#!/usr/bin/env python3
"""waymark-index — regenerate waymark/<status>/index.md from each issue's frontmatter.

Deterministic, stdlib-only, no dependencies. Safe no-op if there is no waymark/ folder.
`updated` is derived from git (last commit date); never hand-written.

Usage: waymark-index.py [repo_root]     (default: current directory)
"""
import os
import re
import subprocess
import sys

STATUSES = ["draft", "approved", "in-progress", "done"]


def parse_frontmatter(path):
    try:
        with open(path, encoding="utf-8") as f:
            text = f.read()
    except OSError:
        return {}
    m = re.match(r"^---\s*\n(.*?)\n---\s*\n", text, re.S)
    if not m:
        return {}
    fm = {}
    for line in m.group(1).splitlines():
        s = line.strip()
        if s and not s.startswith("#") and ":" in line:
            k, _, v = line.partition(":")
            fm[k.strip()] = v.strip().strip('"').strip("'")
    return fm


def git_updated(root, rel_path):
    try:
        out = subprocess.run(
            ["git", "-C", root, "log", "-1", "--format=%cs", "--", rel_path],
            capture_output=True, text=True, timeout=10,
        )
        return out.stdout.strip() or "—"
    except Exception:
        return "—"


def git_created(root, rel_path):
    # First commit date, following renames (files move between status folders via git mv).
    try:
        out = subprocess.run(
            ["git", "-C", root, "log", "--follow", "--format=%cs", "--", rel_path],
            capture_output=True, text=True, timeout=10,
        )
        dates = [d for d in out.stdout.splitlines() if d.strip()]
        return dates[-1] if dates else "—"
    except Exception:
        return "—"


def gen_folder(root, status):
    folder = os.path.join(root, "waymark", status)
    if not os.path.isdir(folder):
        return
    rows = []
    for name in sorted(os.listdir(folder)):
        if not name.endswith(".md") or name == "index.md":
            continue
        fm = parse_frontmatter(os.path.join(folder, name))
        rows.append((
            fm.get("id", name[:-3]),
            fm.get("title", ""),
            fm.get("summary", ""),
            fm.get("assignee", ""),
            git_created(root, f"waymark/{status}/{name}"),
            git_updated(root, f"waymark/{status}/{name}"),
            name,
        ))
    lines = [
        "<!-- ⚙️ AUTO-GENERATED by waymark-index — DO NOT EDIT (regenerated on commit/edit). -->",
        "",
        f"# {status} ({len(rows)})",
        "",
        "| id | title | summary | assignee | created | updated |",
        "|----|-------|---------|----------|---------|---------|",
    ]
    for id_, title, summary, assignee, created, updated, fname in rows:
        lines.append(f"| [{id_}](./{fname}) | {title} | {summary} | {assignee} | {created} | {updated} |")
    lines.append("")
    with open(os.path.join(folder, "index.md"), "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


def main():
    root = sys.argv[1] if len(sys.argv) > 1 else "."
    if not os.path.isdir(os.path.join(root, "waymark")):
        return 0  # no waymark data here — no-op
    for status in STATUSES:
        gen_folder(root, status)
    return 0


if __name__ == "__main__":
    sys.exit(main())
